PhaseSwitchColumnEntry::PhaseSwitchColumnEntry( unsigned char *columnData ) throw ( BaseException )
{
    columnData_ =
        static_cast< unsigned char * >( malloc( PHASESWITCH_COLUMN_BYTE_LENGTH ) );
    
    if ( columnData_ == 0 ) {
        const int saveErrno = errno;

        ostringstream errMsg;
        
        errMsg << "Unable to allocate memory for phase switch column entry: "
               << strerror( saveErrno );
        
        throw CARMA_ERROR( errMsg.str() );
    }
    
    memcpy( columnData_, columnData, PHASESWITCH_COLUMN_BYTE_LENGTH );
}


PhaseSwitchColumnEntry::PhaseSwitchColumnEntry( void ) throw ( BaseException )
{
    columnData_ =
        static_cast< unsigned char * >( malloc( PHASESWITCH_COLUMN_BYTE_LENGTH ) );
    
    if ( columnData_ == 0 ) {
        const int saveErrno = errno;

        ostringstream errMsg;

        errMsg << "Unable to allocate memory for phase switch column entry: "
               << strerror( saveErrno );
        
        throw CARMA_ERROR( errMsg.str() );
    }
    
    memset( columnData_, 0, PHASESWITCH_COLUMN_BYTE_LENGTH );
}


PhaseSwitchColumnEntry *PhaseSwitchColumnEntry::createColumn( short psStepLength, short columnNum )
 throw ( BaseException)
{
  // THIS IS WHERE PHASESWITCH ALGORITHM MAGIC GOES..
  return new PhaseSwitchColumnEntry();
}

void PhaseSwitchColumnEntry::compactEngColumn( short columnId )
{
  memset( columnData_, 0, PHASESWITCH_COLUMN_BYTE_LENGTH );

  for ( int i = 0; i < PHASESWITCH_COLUMN_BYTE_LENGTH; i++ )
   { 
     unsigned char bits[4];

     for ( int j = 0; j < 4; j++ )
     {
       bits[j] = (engPhaseSwitchColumns[(i*4)+j][columnId] == 0  ) ? 0 :
                 (engPhaseSwitchColumns[(i*4)+j][columnId] == 90 ) ? 1 :
                 (engPhaseSwitchColumns[(i*4)+j][columnId] == 180) ? 2 : 3;
//cerr << (i*4)+j << " ";
//cerr << "bits["<<j<<"]: "<<(int)bits[j] <<" ";
//cerr << "(ewc["<<(i*4)+j<<"]["<<columnId<<"]: " << (int)engPhaseSwitchColumns[(i*4)+j][columnId] << ") ";
     }

     columnData_[i] = (bits[0] << 6) | (bits[1] << 4) | (bits[2] << 2) | bits[3];
   }
}


unsigned char PhaseSwitchColumnEntry::getCRC()
{
  unsigned char crc = 0;

  for ( int i = 0; i < PHASESWITCH_COLUMN_BYTE_LENGTH; i++ )
      crc = crcTable[ crc ^ columnData_[i] ];

  return crc;
}

unsigned char *PhaseSwitchColumnEntry::getColumn()
{
  return columnData_;
}


